		<h1>Overview</h1>

		<br><br>
		<span>

		This is a digital archive of primary sources on copyright from the invention of the printing
		press (c. 1450) to the Berne Convention (1886) and beyond. The UK
		<a href="http://www.ahrc.ac.uk/Pages/default.aspx">Arts and Humanities Research
		Council (AHRC)</a> funded the initial phase (completed in 2008) focusing on key materials from
		Renaissance Italy (Venice, Rome), France, the German speaking countries, Britain and the United
		States.
		<br><br>
		We continue to add new materials. Primary sources from Spain were published in 2012; the Netherlands followed in 2015. Jewish Law sources, edited by Neil Netanel, were published in 2016. In 2018, Elena Cooper joined Ronan Deazley as co-editor of the British sources, adding further documents illustrating distinct aspects of the history of copyright concerning the visual arts in the nineteenth century (in the main, 1869-1900). In 2019, Portuguese and Brazilian sources were added by Patrícia Akester and Victor Drummond. In 2021, Jane Ginsburg edited a selection of Vatican documents. 
		<br><br>
		For each of the geographical zones/jurisdictions, national editors take responsibility for selecting, sourcing, transcribing, translating and commenting documents. These include privileges, statutes, judicial decisions, contracts and materials relating to legislative history, but also contemporary letters, essays, treatises and artefacts. <br><br>


		The national editors’ initial brief was to limit the selection to 50 core documents for Britain, Germany, France, and to 20 core documents for Italy and the US (these covering only a shorter period). For the subsequent additions of Spain and The Netherlands, again a limit of 50 core documents was set. For Portuguese, Vatican and Jewish Law sources, a more flexible approach is being used. These sections are still being increased. Some editors have sourced additional contextual documents which are fully catalogued, and linked to the core materials. Document selection is scrutinised by an <a href="index.php?select=editorialboard">international advisory board</a>. Detailed information about our methodological approach may be found in the <a href="https://www.copyrighthistory.org/cam/index.php?select=faq">FAQ</a> section.


