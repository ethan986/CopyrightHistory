        <span class="p0">Update 2018</span>

        <br><br>
        <span class="p3">

                <a href="https://www.copyrighthistory.org/cam/tools/request/browser.php?view=country_record&amp;parameter=Jewish%20Law&amp;country=&amp;core=all">Jewish Law sources</a>, edited by Neil Netanel, were published in 2016. In 2018, a second editor joined the UK section: Dr Elena Cooper, CREATe, University of Glasgow. Elena Cooper contributed further documents to Ronan Deazley's original UK selection, which illustrate distinct aspects of the history of copyright concerning the visual arts in the late nineteenth century (in the main, 1869-1900). Portuguese sources are under development (edited by Patrícia Akester and Victor Drummond).

