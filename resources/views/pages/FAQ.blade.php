		<h1>Methodology</h1>

	<br><br>
	<span>

	
	Information norms (and in particular the laws of intellectual property) are 
	constitutive of modern societies. An understanding of the sources of these norms is critical 
	to understanding the scope and direction of current laws.

	
	<br><br>

	<span>Document Selection</span>

	
	<br><br>

	There is considerable consensus among scholars as to the key points in the intellectual 
	history of copyright law: Invention of printing press (c. 1450); printing privileges 
	(Venice 1469; imperial fairs c16-c17); stationers’ companies (Basel 1531; London 1557); 
	first statutes (England 1710; US 1790); the Enlightenment reprinting debate 
	(“battle of the booksellers”; Diderot; Kant; Fichte); author rights (France 1791/1793; 
	Prussia 1837; UK 1842); Berne Convention (1886). However, there is great controversy about 
	the journey these points mark.
<br><br>
Under one interpretation, a teleological story is told, 
	unfolding from the dark beginnings of privileges to the full recognition of author rights. 
	Under another interpretation, there is an ever-increasing appropriation of the intellectual 
	commons through commercial interests, only briefly interrupted by the public interest rationale 
	asserted in Donaldson v. Becket (1774) and the US Constitution (1787).

	
	<br><br><br>

	<span>Our methodology aims to select documents under three criteria:</span>

	
	<br><br>

	<li>(1) Documents that open up alternative interpretations of copyright history

	
	<br><br>
Particular national copyright laws have come to be associated with distinct 
	philosophical traditions: the US and UK are said to be ‘public interest’-oriented, or 
	utilitarian; France and Germany are regarded as author centric, reflecting deontological 
	philosophical ideas (personality, natural rights). We are interested in 
	documents that affirm, and contradict, these presentations.

	
	<br><br>
Examples include:

	
	<br><br>
Locke had argued against a natural rights approach to copyright 
	in his critique of publishers’ demands after the lapse of the Licensing Act in 1695. 
	However, in Millar v. Taylor (1769), William Murray (later Lord Mansfield) 
	applies a Lockean (“labour theory”) approach to literary property.

	
	<br><br>
Diderot (who also translated Locke) adopted a genius based justification 
	in his Lettre historique et politique adressée à un magistrat sur le commerce de la 
	librairie (1763). There are different versions of this pamphlet which was commissioned 
	and published by the Paris Guild.
<br><br>
Le Chapelier’s report introducing the 1791 
	decree is always quoted with the same line: (‘la plus sacrée, la plus personelle de 
	toutes les propriétés’). This only referred to unpublished works. The rest of the speech 
	emphasises the public interest.
<br><br>
For the editorial comments on such documents, 
	we are particularly interested in bibliographic references that evidence early occurrences 
	of particular interpretations.
<br><br>
</li><li>(2) Documents that illustrate interaction 
	of copyright laws with commercial and aesthetic developments

	
	<br><br>
We are interested in documents that say something about the way in 
	which the law reacted to, and also affected, social circumstances and practices 
	(including technological change, commercial practices and aesthetic practices).

	
	<br><br>
For example: while some legal systems might imagine themselves as establishing 
	a priori categories (eg ‘toutes les beaux arts’), we are interested in documents that 
	indicate when and how those a priori notions were disrupted, contested, reformulated 
	(eg responses to photography in France, at least according to Bernard Edelman). 
	This reflects a more general interest in finding documents that indicate the social 
	conditions that inform the history of copyright. In the UK and the US, the influence 
	of social, technological and economic change may be more obvious, because of the law’s 
	incremental, and reactive development (until 1911).

	
	<br><br>
We are also interested in documents that indicate the impact of copyright 
	regulation on particular creators or publishers (eg Dürer, Hogarth, Beaumarchais, 
	Bourget, Hugo, Weber/Schlesinger, Wordsworth).

	
	<br><br>

	</li><li>(3) Documents that evidence influences across jurisdictions

	
	<br><br>
Copyright histories are often told as if national systems remained hermetically 
	sealed from one another (Sherman &amp; Bently, The Making of Modern Intellectual Property, ch. 11). 
	So we have a British history, a French history etc. We are interested in documents that 
	indicate influence from outside the particular nations. For example, we are interested in 
	evidence that the Venetian privileges constituted a model for licensing systems in France, 
	Germany, England etc, or evidence that suggests there was no such influence 
	(each country independently coming up with the same idea of regulation). 
	Assuming that there was influence, we are interested in documents which tells us 
	about how certain legal systems became models for others.

	
	<br><br>
Examples include: Kant’s 1785 essay, conceiving of books as a ‘speech to the audience’ 
	responded to the literary property debate surrounding Donaldson (1774). 
	The US federal Act of 1790 copies many provisions verbatim from the 1710 
	Statute of Anne, but added a prominent protection for maps and charts 
	(which played an important part in the development of the continent). 
	Prior to the Prussian 1837 Act and the UK 1842 Act, drafts were exchanged 
	and commented upon between the two countries.

	
	<br><br>
We are also interested in documents that indicate the 
	development of national self-consciousness, or national images of 
	copyright. When, where, and how did French, US, German, Italian and 
	UK commentators start to articulate their national laws as different 
	from those of other nations (with different histories, philosophies, functions, concepts etc)? 

	
	<br><br><br>

	<span>Bibliographical approach</span>

	
	<br><br>
Each selected document is introduced by an Abstract, and catalogued according to 
	international library standards, as well as a bibliographical template specifically designed 
	for this project that creates the following browsable categories: referred persons, places, 
	institutions, legislation, case law and 140 subject matter keywords.

	
	<br><br><br>
<span>The Abstract should contain:</span>

	
	<br><br>
(i) Identity of document: This should effectively indicate the type of
document 
	(case, legislation, petition, book).
<br><br>
(ii) Content of document. 
	This should explain the essential features of the
document, for example, 
	that it is a petition relating to all books in
cursive type or legislation 
	protecting sculpture of animals, humans and
invented subjects. It should be 
	pithy, and precise. If it is a book, the focus is on the essential argument: e.g. 
	“this tract was a response to
William Warburton’s 1747 tract (see uk_1747), 
	and argues that there is no
justification for common law rights in literary 
	property based on natural
law”

	
	<br><br>
(iii) Significance of document. 
	Again this should be one or two lines. In many
cases the significance 
	with be related to our overall goals - and the
reason for including the document: 
	so you will want to say that it was “The
first British legislation conferring 
	protection of drama against public
performance, as opposed to reproduction...” 
	“This case firmly established
that protection in Britain was grounded in promoting 
	the public interest,
rather than protecting the inherent rights of authors”...or 
	“This
legislation document indicates the importance of British developments on the

	evolution of copyright laws in Prussia.”

	
	<br><br>
(iv) Content of commentary. A couple of 
	lines on what you say in your
commentary, covering both background description and your 
	argument as to
the significance of the Act.

	
	<br><br>
(v) Bibliography. Suggest a few key bibliographic texts.

