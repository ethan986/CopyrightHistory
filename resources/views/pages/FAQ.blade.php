	<h1>Database technology</h1>

	<br><br>
	<span>

	The first database version from 2008 was based on the open source Kleio system developed 
	by Prof. Manfred Thaller (<a href="http://www.hki.uni-koeln.de" target="blank">HKI Institute</a>, 
	University of Cologne, Germany).
	<br><br>
	In 2011 we redesigned the database in order to provide more 
	flexibility and interconnectivity in regards to the provided data by switching to a
	<a href="http://en.wikipedia.org/wiki/LAMP_(software_bundle)" target="blank">LAMP</a> oriented solution.
	The database now is based on <a href="http://en.wikipedia.org/wiki/CouchDB" target="blank">CouchDB</a>
	(instead of MySql which the term LAMP would indicate). The search is based on 
	<a href="http://en.wikipedia.org/wiki/Solr" target="blank">SOLR</a> which is indexing
	all provided transcription and translation text files.
	All browsing resp. linking mechanisms are 
	<a href="http://en.wikipedia.org/wiki/Php" target="blank">PHP</a> based database requests 
	of specified CouchDB Views.
	</span>
	<br><br><br><br>

	<h1>Links within record and commentary information</h1>
	<br><br>
	<span>

	Every record or commentary information is linked to a database request about that term and its related data. 
	This enables in a convenient way to dig deeper in a specific field of interest (i.e. keywords) when 
	presented on a selected record or commentary.
	<br>
	This is one extension to the first database version where only commentaries and related records were
	linked.
	</span>

